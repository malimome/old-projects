	<h3><a name = "c1">1. Getting Started</a></h3>
	<p>To get started, simply decide to either use our database by browsing over
	to the courses page or upload your own database files by using the XML upload
	feature at the main page.</p>
	<p>For more instructions on how to upload your own database for use with our
	conflict resolving algorithm, please see <a href="#c4">section 4</a>.</p>
	<br></br>
	<h3><a name = "c2">2. Course Selection and Custom Events</a></h3>
	<p>You can select a course by selecting the courses page, and click on the "+"
	button at the far right of the course that is being listed. You can also search
	for a course that you want to add by visiting the search page.</p>
	<p>To delete a course you've added, you should browse over to the schedule page
	and manually delete your course by clicking on the "-" button at the far right
	of the course that is being selected.</p>
	<p>You can also add a custom event by clicking on the custom event page, where
	you can enter information about a custom event that occurs every week, such as
	a part time job and such, and it will fill a slot in your schedule once added.</p>
	<br></br>
	<h3><a name = "c3">3. Managing the Schedule</a></h3>
	<p>The main function of the schedule is to generate conflict free schedules with
	the courses you've selected. To do this, first select the number of courses you
	would like from the list of courses you've selected. The minimum is 1 course, and
	the maximum is 7 courses.</p>
	<p>Next you are to click on the "generate" button. This will generate a list of 
	all the combinations of courses that do not conflict with each other. For example, 
	if you entered a min of 3 and a max of 4, and clicked generate, you would get a list
	of conflict free schedules in combinations of 3 courses and 4 courses.</p>
	<p>Once your schedules are generated, you can either choose to display them online by
	clicking on display, or export it to available formats such as a pdf file by clicking
	on export.</p>
	<br></br>
	<h3><a name = "c4">4. Importing your own database in XML</a></h3>
	<p>If you wish to import your own database, you must provide two xml files, the first
	XML file will contain all the courses in your school or university, and the second
	XML file will contain the courses you've picked from your school or university's
	database. Click on upload file once you have choosen your XML files. Then you will be
	able to generate your schedule and have a choice to download your results in an XML
	file.</p>
